const createMultiplicationGrid = (gridSize) => {
    // Write your code here :white_check_mark:
    /* need a 2d array */
    const maxDigits = (gridSize * gridSize).toString().length;
    const grid = [];

    /* a nested loop to populate my grid */
    for (let i = 1; i <= gridSize; i++) {
        const row = [];
        for (let j = 1; j <= gridSize; j++) {
            row.push((i * j).toString().padStart(maxDigits, ' '));
        }
        grid.push(row.join(' '));
    }

    return grid.join('\n');
}


